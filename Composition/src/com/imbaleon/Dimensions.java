public class Dimensions {
    private int heigh;
    private int width;

    public Dimensions(int heigh, int width) {
        this.heigh = heigh;
        this.width = width;
    }

    public int getHeigh() {
        return heigh;
    }

    public int getWidth() {
        return width;
    }
}
